package com.example.lab6p.bean;

public class Banda {
    private String idBanda;
    private String nombreBanda;
    private int artistaLider;

    public String getIdBanda() {
        return idBanda;
    }

    public void setIdBanda(String idBanda) {
        this.idBanda = idBanda;
    }

    public String getNombreBanda() {
        return nombreBanda;
    }

    public void setNombreBanda(String nombreBanda) {
        this.nombreBanda = nombreBanda;
    }

    public int getArtistaLider() {
        return artistaLider;
    }

    public void setArtistaLider(int artistaLider) {
        this.artistaLider = artistaLider;
    }
}
